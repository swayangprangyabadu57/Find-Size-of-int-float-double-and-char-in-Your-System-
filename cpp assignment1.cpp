#include <iostream>
using namespace std;
int main()
{
	cout << "Size of char:" << sizeof(char) << "byte" << "\n";
	cout << "Size of int:" << sizeof(int) << "byte" << "\n";
	cout << "Size of float:" << sizeof(float) << "byte" << "\n";
	cout << "Size of double:" << sizeof(double) << "byte" << "\n";
	
	return 0;
}
